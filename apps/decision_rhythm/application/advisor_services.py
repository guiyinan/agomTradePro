def _stable_order_intent_id(account_id: str, asset_code: str, side: str, source_id: str) -> str:
    raw = f"{account_id}|{asset_code}|{side}|{source_id}".encode()
    return f"oi_{hashlib.sha1(raw).hexdigest()[:16]}"


