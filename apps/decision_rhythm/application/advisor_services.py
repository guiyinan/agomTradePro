class AdvisorAccessError(PermissionError):
    """Raised when the current user cannot access the account-level sheet."""

    def __init__(self, message: str, status_code: int = 403) -> None:
        super().__init__(message)
        self.status_code = status_code


@dataclass(frozen=True)
class AdvisorHoldingSnapshot:
    """Unified holding snapshot used by the advisor chain."""

    asset_code: str
    asset_name: str
    asset_class: str
    quantity: Decimal
    market_value: Decimal
    current_weight: Decimal
    avg_cost: Decimal
    current_price: Decimal | None
    unrealized_pnl: Decimal
    unrealized_pnl_pct: Decimal
    data_source: str
    price_time: str

    def to_dict(self) -> dict[str, Any]:
        """Return API-safe holding payload."""

        return {
            "asset_code": self.asset_code,
            "asset_name": self.asset_name or self.asset_code,
            "asset_class": self.asset_class,
            "quantity": _decimal_to_number(self.quantity),
            "market_value": _decimal_to_number(self.market_value),
            "current_weight": _decimal_to_number(self.current_weight),
            "avg_cost": _decimal_to_number(self.avg_cost),
            "current_price": _decimal_to_number(self.current_price),
            "unrealized_pnl": _decimal_to_number(self.unrealized_pnl),
            "unrealized_pnl_pct": _decimal_to_number(self.unrealized_pnl_pct),
            "data_source": self.data_source,
            "price_time": self.price_time,
        }


